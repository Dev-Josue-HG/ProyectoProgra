    private void btnAperturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAperturarActionPerformed
        
        txtTittleDatos.setVisible(true);
        txtName.setVisible(true);
        jTextNombre.setVisible(true);
        txtApellidos.setVisible(true);
        jTextApellido.setVisible(true);
        txtNumCuenta.setVisible(true);
        jTextNumCuenta.setVisible(true);
        
        String dpi = jTextDpi.getText();
        String montoIni = jTextMonto.getText();
        String numCuenta = jTextNumCuenta.getText();
        String tipoCuentaU = "";
        
        if(jRadioBtnMonetario.isSelected()){
            tipoCuentaU = "Monetario";
        }else if(jRadioBtnAhorro.isSelected()){
            tipoCuentaU = "Ahorro";
        }else{
            tipoCuentaU = "ERROR";
        }
        
        String tipoCuenta = tipoCuentaU;
        double montoInicial = Double.parseDouble(montoIni);
//        AperturaCuentaUser datosUser = new AperturaCuentaUser();
//        datosUser.apertuarCuenta(dpi);
  
        AperturaCuentaUser guardarRegistro = new AperturaCuentaUser(montoInicial, tipoCuenta ,dpi);
        guardarRegistro.apertuarCuenta();
        guardarRegistro.guardarRegistro(dpi, montoIni, tipoCuenta);
        guardarRegistro.crearArchivoCuenta();
        
        jTextNombre.setText(" " + guardarRegistro.getNombreEncontrado());
        jTextApellido.setText(" " + guardarRegistro.getApellidoEncontrado());
        jTextNumCuenta.setText(" " + guardarRegistro.getNumCuentaA());
        
//        String 
//        guardarRegistro.guardarRegistro(dpi, montoInicial, dpi, dpi, dpi, dpi);
        
    }//GEN-LAST:event_btnAperturarActionPerformed

